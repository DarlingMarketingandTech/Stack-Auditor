const toolsMap = new Map<string, MarTechTool>(martechTools.map((t) => [t.id, t]))

export function getToolById(id: string): MarTechTool | undefined {
  return toolsMap.get(id)
}

export function detectOverlaps(selectedIds: string[]): OverlapWarning[] {
  const warnings: OverlapWarning[] = []
  const seen = new Set<string>()

  for (let i = 0; i < selectedIds.length; i++) {
    for (let j = i + 1; j < selectedIds.length; j++) {
      const a = selectedIds[i]
      const b = selectedIds[j]
      const key = [a, b].sort().join('|')
      if (seen.has(key)) continue

      const toolA = toolsMap.get(a)
      const toolB = toolsMap.get(b)
      if (!toolA || !toolB) continue

      const aOverlapsB = toolA.alternatives?.includes(b) ?? false
      const bOverlapsA = toolB.alternatives?.includes(a) ?? false

      if (aOverlapsB && bOverlapsA) {
        seen.add(key)
        const savings = Math.min(
          toolA.avgMonthlyCost ?? 0,
          toolB.avgMonthlyCost ?? 0,
        )
        warnings.push({
          tools: [a, b],
          message: `${toolA.name} and ${toolB.name} have significant feature overlap`,
          recommendation: `Consider consolidating to one. Keep ${
            (toolA.avgMonthlyCost ?? 0) >= (toolB.avgMonthlyCost ?? 0)
              ? toolB.name
              : toolA.name
          } or evaluate which better fits your workflow.`,
          monthlySavings: savings,
        })
      }
    }
  }

  return warnings
}

export function detectGaps(selectedIds: string[]): GapWarning[] {
  const gaps: GapWarning[] = []
  const selected = selectedIds.map((id) => toolsMap.get(id)).filter(Boolean) as MarTechTool[]

  // Web Analytics gap
  const hasWebAnalytics = selected.some(
    (t) => t.category === 'analytics' && t.subcategory === 'Web Analytics',
  )
  if (!hasWebAnalytics) {
    gaps.push({
      category: 'analytics',
      message: 'No web analytics tool detected',
      recommendation:
        'You need at least one web analytics tool to understand traffic sources and user behaviour. Google Analytics 4 is free and an easy starting point.',
      suggestedTools: ['ga4', 'posthog', 'mixpanel'],
    })
  }

  // CRM gap
  const hasCRM = selected.some((t) => t.category === 'crm')
  if (!hasCRM) {
    gaps.push({
      category: 'crm',
      message: 'No CRM detected — you\'re likely losing leads',
      recommendation:
        'Without a CRM you have no systematic way to track leads or deals. HubSpot CRM has a generous free tier.',
      suggestedTools: ['hubspot', 'pipedrive', 'salesforce'],
    })
  }

  // Attribution gap
  const hasAttribution = selected.some(
    (t) => t.requiredFor?.includes('attribution') ?? false,
  )
  if (!hasAttribution) {
    gaps.push({
      category: 'attribution',
      message: 'No attribution tool — you can\'t measure true ROI',
      recommendation:
        'Without attribution you cannot tell which channels are driving revenue. Start with Google Analytics 4 Goals or Triple Whale for ecommerce.',
      suggestedTools: ['ga4', 'triple-whale', 'northbeam'],
    })
  }

  // Heatmapping gap
  const hasHeatmap = selected.some(
    (t) =>
      t.category === 'analytics' &&
      (t.subcategory.toLowerCase().includes('heatmap') ||
        t.subcategory.toLowerCase().includes('session')),
  )
  if (!hasHeatmap) {
    gaps.push({
      category: 'analytics',
      message: 'No heatmapping / session recording tool',
      recommendation:
        'Heatmapping and session replay are essential for conversion rate optimisation. Microsoft Clarity is free.',
      suggestedTools: ['microsoft-clarity', 'hotjar', 'mouseflow'],
    })
  }

  return gaps
}

export function calculateStackScore(
  selectedIds: string[],
  overlaps: OverlapWarning[],
  gaps: GapWarning[],
): number {
  let score = 100
  const overlapPenalty = Math.min(overlaps.length * 10, 40)
  const gapPenalty = Math.min(gaps.length * 15, 45)
  score -= overlapPenalty + gapPenalty
  return Math.max(0, score)
}

export function getScoreLabel(score: number): string {
  if (score >= 80) return 'Optimized'
  if (score >= 60) return 'Good'
  if (score >= 40) return 'Needs Work'
  return 'Critical'
}

export function runAudit(selectedIds: string[]): AuditResult {
  const selectedTools = selectedIds
    .map((id) => toolsMap.get(id))
    .filter(Boolean) as MarTechTool[]

  const overlaps = detectOverlaps(selectedIds)
  const gaps = detectGaps(selectedIds)
  const stackScore = calculateStackScore(selectedIds, overlaps, gaps)

  const totalMonthlyCost = selectedTools.reduce(
    (sum, t) => sum + (t.avgMonthlyCost ?? 0),
    0,
  )
  const estimatedWaste = overlaps.reduce((sum, o) => sum + o.monthlySavings, 0)

  const recommendations: string[] = []

  if (overlaps.length > 0) {
    recommendations.push(
      `You have ${overlaps.length} tool overlap${overlaps.length > 1 ? 's' : ''}. Eliminating redundant tools could save ~$${estimatedWaste}/month.`,
    )
    overlaps.forEach((o) => {
      const [a, b] = o.tools.map((id) => toolsMap.get(id)?.name ?? id)
      recommendations.push(`Consolidate ${a} and ${b}: ${o.recommendation}`)
    })
  }

  if (gaps.length > 0) {
    gaps.forEach((g) => {
      recommendations.push(`Fill this gap: ${g.message}. ${g.recommendation}`)
    })
  }

  if (stackScore >= 80) {
    recommendations.push(
      'Your stack is well-optimized. Focus on deep feature adoption rather than adding new tools.',
    )
  } else if (stackScore >= 60) {
    recommendations.push(
      'Your stack is in good shape. Address the overlaps above to improve efficiency.',
    )
  } else if (stackScore < 40) {
    recommendations.push(
      'Your stack needs significant attention. Prioritise eliminating redundant tools and filling critical gaps.',
    )
  }

  return {
    stackScore,
    selectedTools,
    overlaps,
    gaps,
    totalMonthlyCost,
    estimatedWaste,
    recommendations,
  }
}
